; --------------------------------------------------------------------------------------------------
; IMPORTANT NOTE:
; --------------------------------------------------------------------------------------------------
; up to 512 lores characters can be displayed by the blink
; 448 ($1C0) from the lores1 bitmap font defined in ROM (granularity 4K)
;  64 ($40)  from the lores0 bitmap font defined in RAM (granularity 512B, only 16 are used on an unexpanded Z88)
;
; organised in 4 parts
;       128 normal lores (32 foreign + 96 ascii)
;       128 bold   lores (same order as normal)
;       128 tiny   lores (same order as normal)
;        64 symbol lores
;
; 10 lores are free
; symbols lores $19E and $19F
; foreign lores $118 to $11F
; --------------------------------------------------------------------------------------------------


